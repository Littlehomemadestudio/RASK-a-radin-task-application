# =============================================================================
# === Digit conversion                                                       ===
# =============================================================================

_FA_DIGITS = "۰۱۲۳۴۵۶۷۸۹"
_EN_DIGITS = "0123456789"
_AR_DIGITS = "٠١٢٣٤٥٦٧٨٩"

_FA_TO_EN = str.maketrans(_FA_DIGITS, _EN_DIGITS)
_EN_TO_FA = str.maketrans(_EN_DIGITS, _FA_DIGITS)
_AR_TO_EN = str.maketrans(_AR_DIGITS, _EN_DIGITS)
_EN_TO_AR = str.maketrans(_EN_DIGITS, _AR_DIGITS)


def to_fa_digits(value) -> str:
    """Convert any Western/Arabic digits in `value` to Persian digits.

    Accepts int, float, or str.  Non-digit characters are passed through
    unchanged.
    """
    s = str(value)
    s = s.translate(_AR_TO_EN)  # normalize arabic first
    return s.translate(_EN_TO_FA)


def to_en_digits(value) -> str:
    """Convert Persian/Arabic digits in `value` to Western digits."""
    s = str(value)
    s = s.translate(_FA_TO_EN)
    s = s.translate(_AR_TO_EN)
    return s


def to_ar_digits(value) -> str:
    """Convert Western/Persian digits to Arabic-Indic digits."""
    s = str(value)
    s = s.translate(_FA_TO_EN)
    return s.translate(_EN_TO_AR)


def localize_digits(value, lang: str = "fa") -> str:
    """Localize digits to the locale's preferred script."""
    if lang == "fa":
        return to_fa_digits(value)
    if lang == "ar":
        return to_ar_digits(value)
    return to_en_digits(value)


# =============================================================================
# === Direction detection                                                     ===
# =============================================================================

RTL_LANGUAGES = frozenset({"fa", "ar", "he", "ur", "yi", "ps", "sd"})


def is_rtl(lang: str) -> bool:
    """Return True if the language is right-to-left."""
    return lang in RTL_LANGUAGES


def text_direction(lang: str) -> str:
    """Return 'rtl' or 'ltr' for the given language."""
    return "rtl" if is_rtl(lang) else "ltr"


# =============================================================================
# === Core translate function                                                 ===
# =============================================================================

_current_lang: str = config.DEFAULT_LANG
_fallback_lang: str = "en"


def set_language(lang: str) -> None:
    """Set the active language.  Falls back to English if unknown."""
    global _current_lang
    if lang in LOCALES:
        _current_lang = lang
    else:
        _current_lang = _fallback_lang


def get_language() -> str:
    return _current_lang


def t(key: str, lang: Optional[str] = None) -> str:
    """Translate a key.  Falls back to English then to the key itself."""
    use_lang = lang or _current_lang
    catalog = LOCALES.get(use_lang) or LOCALES[_fallback_lang]
    if key in catalog:
        return catalog[key]
    if key in LOCALES[_fallback_lang]:
        return LOCALES[_fallback_lang][key]
    return key


def tr(key: str, lang: Optional[str] = None, **kwargs) -> str:
    """Translate with `str.format`-style substitution.

    Example: tr("minutesAgo", n=5) -> "5 minutes ago" / "۵ دقیقه پیش"
    """
    s = t(key, lang)
    try:
        s = s.format(**kwargs)
    except Exception:
        pass
    if (lang or _current_lang) in RTL_LANGUAGES:
        # Convert any digits embedded in the formatted string
        s = to_fa_digits(s) if (lang or _current_lang) == "fa" else s
    return s


def available_locales() -> list[str]:
    """Return list of available locale codes."""
    return sorted(LOCALES.keys())


def register_locale(code: str, catalog: Dict[str, str]) -> None:
    """Register a new locale at runtime.  Merges with English fallback."""
    merged = dict(LOCALES["en"])
    merged.update(catalog)
    LOCALES[code] = merged


def locale_display_name(code: str, in_lang: Optional[str] = None) -> str:
    """Get the display name of a locale, in the requested language."""
    info = config.SUPPORTED_LANGUAGES.get(code, {})
    key = f"name_{in_lang or _current_lang}"
    return info.get(key) or info.get("name_en") or code


# Initialize default language from config
set_language(config.DEFAULT_LANG)



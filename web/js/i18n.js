window.t = function (key, lang) {
  lang = lang || (window.RASK_LANG || "fa");
  const dict = window.I18N[lang] || window.I18N.fa;
  return dict[key] !== undefined ? dict[key] : (window.I18N.en[key] || key);
};

// Persian digit conversion
window.toFaDigits = function (s) {
  return String(s).replace(/[0-9]/g, (d) => "۰۱۲۳۴۵۶۷۸۹"[+d]);
};
